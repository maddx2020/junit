package junit;

public class Calculator {

	public static int getPlus(int x, int y) {
		return x + y;
	}

	public static int getMinus(int x, int y) {
		return x - y;
	}

	public static int getMultiple(int x, int y) {
		return x * y;
	}

	public static double getDivide(double x, double y) {
		return x / y;
	}

}
