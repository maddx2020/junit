package junit;

public class Calculator {

	public double getPlus(double x, double y) {
		return x + y;
	}

	public  double getMinus(double x, double y) {
		return x - y;
	}

	public  double getMultiple(double x, double y) {
		return x * y;
	}

	public  double getDivide(double x, double y) {
		return x / y;
	}

}
